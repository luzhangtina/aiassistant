import SwiftUI

extension Font {
    static func oxanium(size: CGFloat) -> Font {
        return .custom("Oxanium", size: size)
    }
    
    static func darkerGrotesque(size: CGFloat) -> Font {
        return .custom("DarkerGrotesque", size: size)
    }
}
